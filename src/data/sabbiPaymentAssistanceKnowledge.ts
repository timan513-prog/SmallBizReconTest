export const findPaymentAssistanceAnswer = (
  userQuestion: string
): string | null => {
  const normalizedQuestion = userQuestion.toLowerCase().trim();

  // Exact key match
  if (
    paymentAssistanceKnowledgeBase[
      normalizedQuestion as keyof typeof paymentAssistanceKnowledgeBase
    ]
  ) {
    return paymentAssistanceKnowledgeBase[
      normalizedQuestion as keyof typeof paymentAssistanceKnowledgeBase
    ].answer;
  }

  // Search through variations
  for (const [_key, data] of Object.entries(paymentAssistanceKnowledgeBase)) {
    if (
      data.variations.some(
        (variation) =>
          variation.toLowerCase().includes(normalizedQuestion) ||
          normalizedQuestion.includes(variation.toLowerCase())
      )
    ) {
      return data.answer;
    }
  }

  return null;
};

